class Switch
{
public static void main (String[]args)
{
int z = 50;
switch(z)
{
case 1:
System.out.println("the output is k");
break;
case 2:
System.out.println("the output is k");
break;
case 3:
System.out.println("the output is m");
break;
case 4:
System.out.println("the output is n");
break;
default:
System.out.println("the output is o");
}}}